public class Continhas{
    public static void main(String args[]){
        // quero declarar variaveis, reservar espaços na memória.

        int a, b, c;
        a = 19;
        b = 3;

        c = a + b;

        System.out.println("Valor de a = " + a);
        System.out.println("Valor de b = " + b);
        System.out.println("Valor de a + b = " + c);

        c = a - b;
        System.out.println("Valor da subtracao = " + c);

        c = a * b;
        System.out.println("Valor da multiplicação = " + c);

        c = a / b;
        System.out.println("Valor da divisão = " + c);

        c = a % b;
        System.out.println("Valor do resto da divisão = " + c);
    }
}